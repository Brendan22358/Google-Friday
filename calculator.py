class BasicFunctions:
	def __init__(self):
		pass
	def addition(self,a,b):
		self.total=a+b
		return(self.total)
	def subtraction(self,a,b):
		self.total=a-b
		return(self.total)
	def multiplication(self,a,b):
		self.total=a*b
		return(self.total)
	def division(self,a,b):
		self.total=a/b
		return(self.total)
